static int ov10635_probe(struct i2c_client *client,
       const struct i2c_device_id *did)
{
  struct pinctrl *pinctrl;
  struct device *dev = &client->dev;
  int ret;
  u8 chip_id_high, chip_id_low;

  /* ov10635 pinctrl */
  pinctrl = devm_pinctrl_get_select_default(dev);
  if (IS_ERR(pinctrl)) {
    dev_err(dev, "setup pinctrl failed\n");
    return PTR_ERR(pinctrl);
  }

  /* request power down pin */
  pwn_gpio = of_get_named_gpio(dev->of_node, "pwn-gpios", 0);
  if (!gpio_is_valid(pwn_gpio)) {
    dev_err(dev, "no sensor pwdn pin available\n");
    return -ENODEV;
  }
  ret = devm_gpio_request_one(dev, pwn_gpio, GPIOF_OUT_INIT_HIGH,
          "ov10635_pwdn");
  if (ret < 0)
    return ret;

  /* request reset pin */
  rst_gpio = of_get_named_gpio(dev->of_node, "rst-gpios", 0);
  if (!gpio_is_valid(rst_gpio)) {
    dev_err(dev, "no sensor reset pin available\n");
    return -EINVAL;
  }
  ret = devm_gpio_request_one(dev, rst_gpio, GPIOF_OUT_INIT_HIGH,
          "ov10635_reset");
  if (ret < 0)
    return ret;

  /* Set initial values for the sensor struct. */
  memset(&ov10635_data, 0, sizeof(ov10635_data));
  ov10635_data.sensor_clk = devm_clk_get(dev, "csi_mclk");
  if (IS_ERR(ov10635_data.sensor_clk)) {
    dev_err(dev, "get mclk failed\n");
    return PTR_ERR(ov10635_data.sensor_clk);
  }

  ret = of_property_read_u32(dev->of_node, "mclk", 
      &ov10635_data.mclk);
  if (ret) {
    dev_err(dev, "mclk frequency is invalid\n");
    return ret;
  }

  ret = of_property_read_u32(dev->of_node, "mclk_source",
      (u32 *) &(ov10635_data.mclk_source));
  if (ret) {
    dev_err(dev, "mclk_source invalid\n");
    return ret;
  }

  ret = of_property_read_u32(dev->of_node, "csi_id",
      &(ov10635_data.csi));
  if (ret) {
    dev_err(dev, "csi_id invalid\n");
    return ret;
  }

  clk_prepare_enable(ov10635_data.sensor_clk);

  ov10635_data.io_init = ov10635_reset;
  ov10635_data.i2c_client = client;
  ov10635_data.pix.pixelformat = V4L2_PIX_FMT_UYVY;
  ov10635_data.pix.width = 640;
  ov10635_data.pix.height = 480;
  ov10635_data.streamcap.capability = V4L2_MODE_HIGHQUALITY |
              V4L2_CAP_TIMEPERFRAME;
  ov10635_data.streamcap.capturemode = 0;
  ov10635_data.streamcap.timeperframe.denominator = OV10635_DEFAULT_FPS;
  ov10635_data.streamcap.timeperframe.numerator = 1;

  ov10635_regulator_enable(&client->dev);

  ov10635_reset();

  ov10635_power_down(0);

  ret = ov10635_reg_read(client, OV10635_CHIP_ID_HIGH_BYTE, &chip_id_high);
  if (ret < 0 || chip_id_high != 0xA6) {
    clk_disable_unprepare(ov10635_data.sensor_clk);
    pr_warning("camera ov10635 is not found\n");
    return -ENODEV;
  }

  ret = ov10635_reg_read(client, OV10635_CHIP_ID_LOW_BYTE, &chip_id_low);
  if (ret < 0 || chip_id_low != 0x35) {
    clk_disable_unprepare(ov10635_data.sensor_clk);
    pr_warning("camera ov10635 is not found\n");
    return -ENODEV;
  }

  ov10635_power_down(1);

  clk_disable_unprepare(ov10635_data.sensor_clk);

  ov10635_int_device.priv = &ov10635_data;
  ret = v4l2_int_device_register(&ov10635_int_device);

  pr_info("camera ov10635 is found\n");
  return ret;

}
/*!
 * ov10635 I2C detach function
 *
 * @param client            struct i2c_client *
 * @return  Error code indicating success or failure
 */
static int ov10635_remove(struct i2c_client *client)
{
  v4l2_int_device_unregister(&ov10635_int_device);

  if (analog_regulator)
    regulator_disable(analog_regulator);

  if (core_regulator)
    regulator_disable(core_regulator);

  if (io_regulator)
    regulator_disable(io_regulator);
  pr_info("camera ov10635 is removed\n");

  return 0;
}

