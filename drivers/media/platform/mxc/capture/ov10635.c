/* reset the camera module */
static inline void ov10635_reset(void)
{
  /* camera reset */
  gpio_set_value(rst_gpio, 1);

  /* camera power down */
  gpio_set_value(pwn_gpio, 1);
  msleep(5);
  gpio_set_value(pwn_gpio, 0);
  msleep(5);
  gpio_set_value(rst_gpio, 0);
  msleep(1);
  gpio_set_value(rst_gpio, 1);
  msleep(5);
  gpio_set_value(pwn_gpio, 1);
}

static void ov10635_soft_reset(void)
{
  /* sysclk from pad */
  ov10635_reg_write(ov10635_data.i2c_client, 0x3103, 0x11);

  /* software reset */
  ov10635_reg_write(ov10635_data.i2c_client, 0x3008, 0x82);

  /* delay at least 5ms */
  msleep(10);
}

static int ov10635_init_mode(enum ov10635_frame_rate frame_rate,
        enum ov10635_mode mode)
{
  const struct ov10635_reg *pModeSetting = NULL;
  int pModeSize = 0, ret = 0;
  u8 val;

  ov10635_soft_reset();

  /* change driver capability to 2x according to validation board.
  * if the image is not stable, please increase the driver strength.
  */
  ov10635_driver_capability(ov10635_data.i2c_client, 2);

  pModeSetting = ov10635_mode_info_data[frame_rate][mode].init_data_ptr;
  pModeSize = ov10635_mode_info_data[frame_rate][mode].init_data_size;

  ov10635_data.pix.width = ov10635_mode_info_data[frame_rate][mode].width;
  ov10635_data.pix.height = ov10635_mode_info_data[frame_rate][mode].height;

  ret = ov10635_set_regs(ov10635_data.i2c_client, pModeSetting, pModeSize);
  if (ret < 0)
    goto err;

  /* skip 9 vysnc: start capture at 10th vsync */
  msleep(300);

err:
  return ret;
}

/* change to or back to subsampling mode set the mode directly
 * image size below 1280 * 960 is subsampling mode */
static int ov10635_change_mode_direct(enum ov10635_frame_rate frame_rate,
          enum ov10635_mode mode)
{
  int ret = 0;
  struct ov10635_reg *pModeSetting = NULL;
  int pModeSize = 0;

  if (mode > ov10635_mode_MAX || mode < ov10635_mode_MIN) {
    pr_err("Wrong ov10635 mode detected!\n");
    return -1;
  }

  pModeSetting = ov10635_mode_info_data[frame_rate][mode].init_data_ptr;
  pModeSize = ov10635_mode_info_data[frame_rate][mode].init_data_size;

  ov10635_data.pix.width = ov10635_mode_info_data[frame_rate][mode].width;
  ov10635_data.pix.height = ov10635_mode_info_data[frame_rate][mode].height;
  
  if (ov10635_data.pix.width == 0 || ov10635_data.pix.height == 0 ||
    pModeSetting == NULL || pModeSize == 0)
      return -EINVAL;

  /* set ov10635 to subsampling mode */
  ret = ov10635_set_regs(ov10635_data.i2c_client, pModeSetting, pModeSize);

  /* turn on AE AG for subsampling mode, in case the firmware didn't */
  ov10635_turn_on_AE_AG(1);

  /* calculate banding filter */
  ov10635_set_bandingfilter();

  /* set AE target */
  ov10635_set_AE_target(AE_Target);

  /* update night mode setting */
  ov10635_set_night_mode(night_mode);

  if (frame_rate == ov10635_15_fps) {
    /* 1/15 * 9*/
    msleep(600);
  } else if (frame_rate == ov10635_30_fps) {
    /* 1/30 * 9*/
    msleep(300);
  }

  return ret;
}

static int ov10635_change_mode(enum ov10635_frame_rate frame_rate,
            enum ov10635_mode mode)
{
  int ret = 0;

  if (mode > ov10635_mode_MAX || mode < ov10635_mode_MIN) {
    pr_err("Wrong ov10635 mode detected!\n");
    return -1;
  }

  /* change back to subsampling mode */
  ret = ov10635_change_mode_direct(frame_rate, mode);

  return ret;
}

