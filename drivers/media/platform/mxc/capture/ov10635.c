/* set sensor output drive capability
 * 0x3011[7:6] - strength
 * 00     - 1x
 * 01     - 2x
 * 10     - 3x
 * 11     - 4x
 */
static int ov10635_driver_capability(struct i2c_client *client, int strength)
{
  u8 temp = 0;

  if (strength > 4 || strength < 1) {
    pr_err("The valid driver capability of ov10635 is 1x~4x\n");
    return -EINVAL;
  }

  ov10635_reg_read(client, OV10635_DRIVING_CAPABILITY, &temp);

  temp &= ~0xc0;	/* clear [7:6] */
  temp |= ((strength - 1) << 6);	/* set [7:6] */

  ov10635_reg_write(client, OV10635_DRIVING_CAPABILITY, temp);

  return 0;
}

