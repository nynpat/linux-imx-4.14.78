/*!
 * ioctl_dev_init - V4L2 sensor interface handler for vidioc_int_dev_init_num
 * @s: pointer to standard V4L2 device structure
 *
 * Initialise the device when slave attaches to the master.
 */
static int ioctl_dev_init(struct v4l2_int_device *s)
{
  struct sensor_data *sensor = s->priv;
  u32 tgt_xclk;  /* target xclk */
  u32 tgt_fps;  /* target frames per secound */
  enum ov10635_frame_rate frame_rate;
  enum ov10635_mode mode = ov10635_mode_VGA_640_480;
  int ret;

  ov10635_data.on = true;

  /* mclk */
  tgt_xclk = ov10635_data.mclk;
  tgt_xclk = min(tgt_xclk, (u32)OV10635_XCLK_MAX);
  tgt_xclk = max(tgt_xclk, (u32)OV10635_XCLK_MIN);
  ov10635_data.mclk = tgt_xclk;

  pr_debug("Setting mclk to %d MHz\n", tgt_xclk / 1000000);
  clk_set_rate(ov10635_data.sensor_clk, ov10635_data.mclk);

  /* Default camera frame rate is set in probe */
  tgt_fps = (sensor->streamcap.timeperframe.denominator /
    sensor->streamcap.timeperframe.numerator);

  if (tgt_fps == 15)
    frame_rate = ov10635_15_fps;
  else if (tgt_fps == 30)
    frame_rate = ov10635_30_fps;
  else
    return -EINVAL; /* Only support 15fps or 30fps now. */

  ret = ov10635_init_mode(frame_rate, mode);
  
  return ret;
}

