  fsize->discrete.width = max(ov10635_mode_info_data[0][fsize->index].width,
                      ov10635_mode_info_data[1][fsize->index].width);
  fsize->discrete.height = max(ov10635_mode_info_data[0][fsize->index].height,
                      ov10635_mode_info_data[1][fsize->index].height);

